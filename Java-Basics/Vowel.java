import java.lang.*;

public class Vowel {
    public static void main(String[] args) {
        String ch = "a";
        if (ch == "a" || ch == "e" || ch == "i" || ch == "o" || ch == "u") {
            System.out.println("This is an Vowel Character");
        } else {
            System.out.println("This is an Consonent Character");
        }
    }
}
